public class Cuse {
	public String mName;
	public int mTime;

	public Cuse() {
	}

	public Cuse(String mName, int mTime) {
		this.mName = mName;
		this.mTime = mTime;
	}

	public String getmName() {
		return mName;
	}

	public void setmName(String mName) {
		this.mName = mName;
	}

	public int getmTime() {
		return mTime;
	}

	public void setmTime(int mTime) {
		this.mTime = mTime;
	}
}
